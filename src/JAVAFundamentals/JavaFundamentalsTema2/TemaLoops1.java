package JAVAFundamentals.JavaFundamentalsTema2;

public class TemaLoops1 {
    public static void main(String[] args) {
        int n = 10;
        while (n < 50) {
            n = n * 2;
        }
        if (n == 10) {
            System.out.println("A");

        }
        if (n == 40) {
            System.out.println("B");
        }
        if (n == 50) {
            System.out.println("C");
        }
        if (n == 80) {
            System.out.println("D");
        }
    }
}
